;==============================================================
; delay_2ms_asm: программная задержка 2 мс при F_CPU = 8 МГц
; Вариант 6а (по аналогии с ЛР1):
;   внутренний цикл — INC R29, NOP, BRNE
;   внешний  цикл — NOP, INC R30, BRNE
;
; Требуемое число тактов: t · f = 0,002 · 8 000 000 = 16 000
;
; Формула (см. ЛР1): N = 4·(256 − x) + 1035 + (254 − y)·1027 + n
;   x — начальное значение R29,  y — R30,  n — доп. NOP перед RET.
;
; Подставляя x = 110, y = 240, n = 3:
;   4·146 + 1035 + 14·1027 + 3 = 584 + 1035 + 14378 + 3 = 16 000  ✓
;==============================================================

#include <avr/io.h>

.global delay_2ms_asm
.type   delay_2ms_asm, @function

delay_2ms_asm:
        ldi     r30, 240        ; y = 240 (внешний счётчик R30)
        ldi     r29, 110        ; x = 110 (внутренний счётчик R29)

delay_sub:
        inc     r29             ; 1 такт
        nop                     ; 1 такт
        brne    delay_sub       ; 2 такта при переходе, 1 — без
        nop                     ; 1 такт — внешний NOP
        inc     r30             ; 1 такт
        brne    delay_sub       ; 2 / 1

        nop                     ; 3 дополнительных NOP для точной
        nop                     ; подгонки до 16 000 тактов
        nop
        ret                     ; 4 такта
