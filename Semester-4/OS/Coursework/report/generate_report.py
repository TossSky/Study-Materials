def source(num, text, url=None):
    """Запись в списке использованных источников.

    Первый аргумент — описание без URL. Второй — URL, если ресурс электронный,
    выводится на новой строке (soft line break) с префиксом «URL: ».

    Выравнивание по левому краю — чтобы длинные URL не растягивали строку.
    Висячий отступ 0.75 см для удобства чтения.
    """
    para = doc.add_paragraph()
    para.paragraph_format.first_line_indent = Cm(-0.75)
    para.paragraph_format.left_indent = Cm(0.75)
    para.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.LEFT
    para.paragraph_format.space_after = Pt(6)

    run = para.add_run(f'{num}. {text}')
    run.font.name = 'Times New Roman'
    run.font.size = Pt(14)

    if url:
        run.add_break()  # soft line break (Shift+Enter) — URL на новой строке
        run2 = para.add_run(f'URL: {url}')
        run2.font.name = 'Times New Roman'
        run2.font.size = Pt(14)
    return para


