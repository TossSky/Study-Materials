def _tune_heading(name, size, bold=True, upper=False, align=WD_ALIGN_PARAGRAPH.LEFT):
    st = doc.styles[name]
    st.font.name = 'Times New Roman'
    st.font.size = Pt(size)
    st.font.bold = bold
    st.font.italic = False
    st.font.color.rgb = None  # чёрный
    # Сбрасываем theme-цвет, который ставит Word по умолчанию для Heading
    rPr = st.element.get_or_add_rPr()
    for tag in ('color',):
        for el in rPr.findall(qn(f'w:{tag}')):
            rPr.remove(el)
    col = OxmlElement('w:color')
    col.set(qn('w:val'), '000000')
    rPr.append(col)
    st.paragraph_format.space_before = Pt(0)
    st.paragraph_format.space_after = Pt(12)
    st.paragraph_format.first_line_indent = Cm(0)
    st.paragraph_format.line_spacing = 1.5
    st.paragraph_format.alignment = align
    st.paragraph_format.keep_with_next = True


