def make_simple_table(header, rows, widths_cm=None):
    """Таблица с видимыми границами, Times New Roman 14, полуторный интервал
    внутри — по ГОСТ 7.32-2017 (в таблицах допускается меньший кегль, но
    для курсовой оставляем 14)."""
    from docx.enum.table import WD_ALIGN_VERTICAL
    t = doc.add_table(rows=1 + len(rows), cols=len(header))
    t.style = 'Table Grid'
    if widths_cm:
        set_col_widths(t, widths_cm)

    def _fill(cell, text, bold=False):
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
        cell.text = ''
        para = cell.paragraphs[0]
        para.paragraph_format.first_line_indent = Cm(0)
        para.paragraph_format.line_spacing = 1.15
        para.paragraph_format.space_after = Pt(0)
        para.alignment = WD_ALIGN_PARAGRAPH.LEFT
        r = para.add_run(text)
        r.font.name = 'Times New Roman'
        r.font.size = Pt(12)
        r.bold = bold

    for i, h in enumerate(header):
        _fill(t.cell(0, i), h, bold=True)
    for ri, row in enumerate(rows, start=1):
        for ci, val in enumerate(row):
            _fill(t.cell(ri, ci), val)
    # Пустой абзац после таблицы — чтобы следующий текст не слипался
    p_after = doc.add_paragraph()
    p_after.paragraph_format.first_line_indent = Cm(0)
    p_after.paragraph_format.space_after = Pt(0)
    return t


