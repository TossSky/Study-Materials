task(3, 'Целочисленный максимальный поток')
cond('если все пропускные способности c(e) целочисленные, то существует '
     'максимальный поток, значения которого на всех рёбрах целые.')

section('Что пишем на доске и рисуем.')
par('Достаточно показать, что алгоритм Форда–Фалкерсона, начатый с нулевого '
    'потока, сохраняет целочисленность на каждой итерации. Из этого автоматически '
    'следует, что его результат — целочисленный поток; а по теореме '
    'max-flow/min-cut он ещё и максимальный.')

par('Ведём индукцию по номеру итерации.')

step(1, 'f₀ ≡ 0 — целый.')
step(2, 'пусть fₖ целый. Остаточные ёмкости')
formula('c_res(u, v) = c(u, v) − fₖ(u, v) + fₖ(v, u)')
par('— это разности целых чисел, следовательно, тоже целые.')
step(3, 'bottleneck увеличивающего пути δ = min c_res(e) — минимум целых, '
        'значит, целое.')
step(4, 'обновление fₖ₊₁ = fₖ ± δ сохраняет целочисленность.')

par('Завершение. Величина |f| возрастает не меньше чем на 1 за шаг, а сверху '
    'ограничена Σ c(s, ·). В момент отсутствия увеличивающих путей поток '
    'максимален (теорема Форда–Фалкерсона) и при этом целый. ∎')

picture('fig_04_integer_ff.png', width_cm=15.5,
        cap='Рисунок 4 — пример: на каждой итерации bottleneck δ остаётся целым')

section('Что говорим.')
par('Утверждение доказывается через инвариант алгоритма Форда–Фалкерсона: '
    'если стартовать с нулевого потока, целочисленность сохраняется на каждой '
    'итерации. Ведём индукцию. База: f₀ ≡ 0 — целый. Индукционный шаг: пусть '
    'поток fₖ целый. Остаточные пропускные способности равны c минус '
    'fₖ u v плюс fₖ v u — это разности целых, значит, тоже целые. Bottleneck '
    'увеличивающего пути δ — минимум по целым, опять целое. Новый поток '
    'fₖ₊₁ = fₖ ± δ получается прибавлением или вычитанием целого, значит '
    'тоже целый. Остаётся показать завершение: на каждой итерации величина '
    'потока возрастает хотя бы на единицу, потому что δ — положительное '
    'целое, а сверху она ограничена суммой пропускных способностей исходящих '
    'из источника рёбер. Значит, алгоритм завершится за конечное число шагов, '
    'и по теореме Форда–Фалкерсона в этот момент поток максимален. По '
    'построению он целочисленный.')


