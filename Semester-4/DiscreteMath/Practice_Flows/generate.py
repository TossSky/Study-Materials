formula("f ′ = (y / x) · (α₁·P₁ + … + αₖ·Pₖ).")

par('Проверка:')
bullet('линейная комбинация путевых потоков — поток;')
bullet('|f ′| = (y/x) · Σ αᵢ = y;')
bullet('y/x < 1  ⟹  f ′(e) ≤ f(e) ≤ c(e) для всех рёбер, ограничения '
       'выполнены. ∎')

picture('fig_02_scaling.png', width_cm=15,
        cap='Рисунок 1 — масштабирование коэффициентом y/x даёт поток величины y')

section('Что говорим.')
par('Идея в том, что любой поток раскладывается на путевые компоненты — '
    'потоки по путям из источника в сток — и циклические компоненты. При этом '
    'величина потока определяется только путевыми компонентами: циклы '
    'замкнуты и в величину не вносят вклад. Значит, чтобы получить поток '
    'меньшей величины y, достаточно пропорционально уменьшить путевую часть. '
    'Разложим f по теореме разложения: он равен сумме α i на P i плюс '
    'циклическим компонентам, а сумма коэффициентов α i равна x. Положим '
    'f штрих равным y, делённому на x, и умноженному на путевую часть. Это '
    'снова поток — линейная комбинация потоков есть поток. Его величина '
    'равна y/x, умноженному на сумму α i, то есть равна y. Наконец, y/x '
    'меньше единицы, поэтому новые значения на рёбрах строго меньше '
    'исходных и точно не выходят за пропускные способности. Поток величины '
    'y построен.')


