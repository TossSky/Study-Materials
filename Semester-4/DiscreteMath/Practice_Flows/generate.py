task(2, 'Разложение потока на пути и циклы')
cond('всякий поток f в сети можно представить как сумму потоков по путям s → t и '
     'потоков по циклам.')

section('Что пишем на доске и рисуем.')
par('Доказательство конструктивное: по одному выделяем из потока либо путь '
    's → t, либо цикл, и вычитаем его. Каждое такое вычитание обнуляет хотя бы '
    'одно ребро, поэтому за конечное число шагов разложим весь поток.')

par('Ведём индукцию по числу рёбер с f(e) > 0. База: f ≡ 0 — сумма пуста. Шаг: '
    'возьмём любое ребро (u, v) с f(u, v) > 0. Запускаем поиск по рёбрам с '
    'положительным потоком от v вперёд и от u назад. Сохранение потока '
    'гарантирует, что из каждой внутренней вершины есть куда продолжить обход. '
    'Возникает ровно один из двух случаев:')

picture('fig_03_traversal.png', width_cm=16,
        cap='Рисунок 2 — два случая обхода: путь до t либо возврат в '
            'посещённую вершину')

step('А', 'поиск дошёл до t (параллельно из u дотянулся до s) — выделена '
         'структура-путь P: s → … → u → v → … → t;')
step('Б', 'поиск вернулся в уже посещённую вершину — выделен цикл C.')

par('Возьмём узкое место δ = min f(e) по рёбрам структуры и определим новый '
    'поток:')
formula('f₁(e) = f(e) − δ  на рёбрах структуры;   f₁(e) = f(e)  иначе.')

par('Сохранение потока в f₁ выполняется автоматически (из всех рёбер структуры '
    'вычли одинаковый δ). Хотя бы на одном ребре f₁(e) = 0 — структура '
    '«сжала» его до нуля. По индукции f₁ разлагается в требуемую сумму; '
    'добавив к ней исходную структуру с коэффициентом δ, получаем разложение '
    'для f. Процесс завершается не более чем за |E| шагов. ∎')

picture('fig_01_decomposition.png', width_cm=13.5,
        cap='Рисунок 3 — пример: поток раскладывается на два пути и один цикл')

section('Что говорим.')
par('Доказательство конструктивное — покажем, как алгоритмически извлечь '
    'разложение. Ведём индукцию по числу рёбер с положительным потоком. База: '
    'если поток тождественно нулевой, разложение пустое. Индукционный шаг: '
    'берём любое ребро u v с положительным потоком и запускаем обход — от '
    'вершины v движемся вперёд по рёбрам с положительным потоком, параллельно '
    'от u — назад. Сохранение потока гарантирует, что из каждой внутренней '
    'вершины есть куда продолжить. Возможны два случая. Случай А: поиск дошёл '
    'до стока t, а обратный обход от u — до источника s; тогда выделена '
    'структура-путь от s до t, проходящая через ребро u v. Случай Б: поиск '
    'вернулся в уже посещённую вершину — тогда выделен цикл. В обоих случаях '
    'берём минимум потока по рёбрам этой структуры — обозначим δ — и вычитаем '
    'его. Условие сохранения не нарушается, потому что из всех рёбер структуры '
    'вычли одинаковое число. Хотя бы на одном ребре — там, где был минимум — '
    'новый поток обнуляется, число ненулевых рёбер строго уменьшается. По '
    'индукции остаток разлагается в сумму; добавив к ней исходную структуру '
    'с коэффициентом δ, получаем разложение для f. Процесс завершается не '
    'более чем за |E| шагов.')


