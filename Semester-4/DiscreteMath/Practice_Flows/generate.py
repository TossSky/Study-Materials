task(4, 'Контрпример Форда–Фалкерсона на вещественных')
cond('построить сеть с вещественными пропускными способностями, на которой '
     'алгоритм Форда–Фалкерсона при неудачном выборе увеличивающих путей '
     'никогда не завершается.')

section('Что пишем на доске и рисуем.')
par('Форд–Фалкерсон в исходной формулировке не специфицирует, как выбирать '
    'увеличивающий путь. Если ёмкости иррациональны, плохой выбор приводит к '
    'геометрической прогрессии приростов, сумма которой конечна, но меньше '
    'максимального потока — алгоритм не достигает оптимума.')

par('Рассмотрим обратное золотое сечение')
formula('φ = (√5 − 1) / 2 ≈ 0.618,   со свойством φ² = 1 − φ.')

par('В сети s, t и две внутренние вершины a, b. Четыре «толстых» ребра с '
    'ёмкостями M соединяют s с a, b и a, b с t. Между a и b — два тонких ребра '
    'с ёмкостями 1 и φ.')

picture('fig_05_golden.png', width_cm=13.5,
        cap='Рисунок 5 — сеть Форда–Фалкерсона: ёмкости M, 1 и φ')

par('При жадном выборе пути (через тонкое ребро с наименьшей оставшейся '
    'ёмкостью) приросты потока образуют последовательность')
formula('1, φ, φ², φ³, …')

par('Сумма этого ряда конечна и строго меньше максимального потока 2M + 1, '
    'поэтому алгоритм бесконечно итерируется и не сходится к оптимуму.')

section('Что говорим.')
par('Это известный контрпример к наивной версии алгоритма. Форд–Фалкерсон не '
    'специфицирует, как выбирать увеличивающий путь — если ёмкости '
    'рациональны, алгоритм всегда завершается, а для иррациональных ёмкостей '
    'плохой выбор может привести к проблемам. Возьмём обратное золотое '
    'сечение φ — корень из пяти минус единица, делённое на два, приблизительно '
    '0.618; оно удовлетворяет уравнению φ² = 1 − φ, и это ключевое свойство. '
    'Строим сеть: источник s, сток t, две внутренние вершины a и b. Четыре '
    '«толстых» ребра ёмкости M — от s к a и b и от a и b к t. Между a и b — '
    'два «тонких» ребра ёмкостей 1 и φ. Если на каждой итерации выбирать '
    'увеличивающий путь жадно, через тонкое ребро с наименьшей оставшейся '
    'ёмкостью, то приросты потока образуют геометрическую прогрессию 1, φ, '
    'φ², φ³ и так далее. Сумма такой прогрессии конечна, равна 1 + φ/(1 − φ), '
    'что строго меньше максимального потока 2M + 1. Алгоритм бесконечно '
    'итерируется, а значение потока сходится к числу, меньшему оптимума. На '
    'практике эта проблема снимается алгоритмом Эдмондса–Карпа — он выбирает '
    'увеличивающий путь поиском в ширину, то есть кратчайший, и '
    'гарантированно завершается за O(V · E²) независимо от ёмкостей.')


